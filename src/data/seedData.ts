// ============================================================
// LOCAL STORAGE HELPERS
// ============================================================
const STORAGE_KEY = 'nas_cikolata_v4';

export function loadState(): AppState {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (raw) {
      const parsed = JSON.parse(raw) as Partial<AppState>;

      // Always sync key fields from seed data so logic/CDN updates are reflected
      const mergedProducts = (parsed.products ?? defaultState.products).map(p => {
        const seed = defaultState.products.find(s => s.id === p.id);
        if (seed) return { ...p, imageUrl: seed.imageUrl, gallery: seed.gallery, categoryId: seed.categoryId, subCategoryId: seed.subCategoryId, type: seed.type, subType: seed.subType };
        return p;
      });
      // Append any new products that exist in defaultState but not in parsed
      const existingProductIds = new Set(mergedProducts.map(p => p.id));
      for (const sp of defaultState.products) {
        if (!existingProductIds.has(sp.id)) mergedProducts.push(sp);
      }
      const mergedCategories = (parsed.categories ?? defaultState.categories).map(c => {
        const seed = defaultState.categories.find(s => s.id === c.id);
        if (seed) return { ...c, imageUrl: seed.imageUrl };
        return c;
      });

      return {
        orders: parsed.orders ?? defaultState.orders,
        products: mergedProducts,
        categories: mergedCategories,
        subCategories: parsed.subCategories ?? defaultState.subCategories,
        payments: parsed.payments ?? defaultState.payments,
        settings: { ...defaultSettings, ...(parsed.settings ?? {}) },
      };
    }
  } catch (_) { /* ignore */ }
  return { ...defaultState };
}


export function saveState(state: AppState) {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
  } catch (_) { /* ignore */ }
}

// ============================================================
// ORDER CODE GENERATOR
// ============================================================
export function generateOrderCode(existing: string[]): string {
  const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  let code = '';
  do {
    code = Array.from({ length: 5 }, () => chars[Math.floor(Math.random() * chars.length)]).join('');
  } while (existing.includes(code));
  return code;
}

